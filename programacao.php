							</p>
							
							<p align="justify">
								O programa de recuperação da CTNJ tem uma duração de nove (9) meses, e está dividido em três (3) etapas, sendo estas:
							</p>
							
							<p align="justify">
								<ul>
									<li><strong><em>1ª etapa</em></strong> - 1º ao 3º mês: Desintoxicação e Adaptação.</li>
									<li><strong><em>2ª etapa</em></strong> - 3º ao 6º mês: Conscientização.</li>
									<li><strong><em>3ª etapa</em></strong> - 6º ao 9º mês: Ressocialização.</li>
								</ul>
							</p>
							
							<p align="justify">
								A <strong>primeira etapa</strong> começa ao ingressar na comunidade e se estende até o terceiro mês. Esta etapa é chamada de <strong>"Adaptação e Desintoxicação"</strong>, sendo os primeiros objetivos a serem atingidos pelo dependente químico em recuperação: adaptação ao grupo e ás normas da comunidade, desintoxicação física das drogas e reestabilização emocional e mental.
							</p>
							
							<p align="justify">
								A <strong>segunda etapa</strong> começa no terceiro mês e se estende até o sexto, e é chamada de <strong>"Conscientização"</strong>. Nesta etapa acontece o verdadeiro objetivo do tratamento, que é a mudança de caráter. Através da  conscientização  da  doença,  dos  defeitos  de  caráter, da derrota total e da necessidade de mudança, o dependente consegue reconstituir a sua vida e a sua moral.
							</p>
							
							<p align="justify">
								Nos três últimos meses o dependente entra na <strong>terceira etapa</strong> que é chamada de <strong>"Ressocialização"</strong>, e consiste no retorno sistemático à sociedade, sendo este objetivo final do tratamento. Durante estes três meses o dependente realiza uma visita mensal de uma semana à família, para poder avaliar no seu ambiente de convivência os ensinamentos aprendidos durante os meses anteriores. Esta etapa também é importante para a família, que também deve ter se preparado para este momento.
