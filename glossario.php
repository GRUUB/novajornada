					<div class="row">
						<div class="col-lg-12 col-md-12 col-sm-12 col-xs-12">
							<h4><strong>12 Passos – Estudo</strong></h4>
							
							<p align="justify">
								O programa de recuperação dos 12 Passos é uma metodologia mundialmente conhecida e aceita, tanto entre as equipes “não profissionais” quanto entre muitos dos profissionais de todas as áreas da saúde. Mesmo sendo uma metodologia não científica, desenvolvida por alcoólicos em recuperação (AA), com o subsídio de profissionais da saúde, mostrou ser, ao longo dos últimos 60 anos, uma ferramenta indispensável para todos aqueles que se esforçam consistentemente na recuperação da dependência do álcool e de outras drogas.
								<br />
								As reuniões de 12 Passos são divididas em 3 grupos, de acordo com a Fase do tratamento. Assim, quem está na 1ª Fase estuda do 1º ao 3º Passo, na 2ª Fase estuda o 4º e o 5º Passo, e também participa eventualmente das reuniões da 3ª Fase que estuda do 6º ao 12º Passo.
								<br />
								No final de cada Passo é aplicada uma prova escrita com a finalidade de avaliar os conhecimentos adquiridos sobre o assunto. Se o residente não atingir a pontuação mínima de 5 pontos deverá copiar o Passo estudado para poder passar para o estudo do próximo Passo.
							</p>
							
							<div class="space-20"></div>
							
							<h4><strong>4º Passo – Escrita</strong></h4>
							
							<p align="justify">
								Ao completar 3 meses de tratamento, e ingressar na 2ª Fase do mesmo, o residente recebe o Guia de 4º Passo, elaborado pelo Coordenador da CTNJ, que consiste numa relação de 374 perguntas que o residente deverá responder por escrito até o 5º mês de tratamento, quando começara o 5º Passo.
								<br />
								Estas perguntas podem ser respondidas com a ajuda da família e do Padrinho, e levam o residente a fazer uma profunda avaliação da sua história de vida, as suas relações, as suas conquistas e as suas derrotas, os danos que possa ter causado a outras pessoas, assim como os danos que sofreu em relacionamentos disfuncionais. Enfim, é uma poderosa ferramenta de autoconhecimento, fator básico para iniciar uma vida diferente, de qualidade, após o tratamento.
							</p>
							
							<div class="space-20"></div>
							
							<h4><strong>4º Passo – Padrinho</strong></h4>
							
							<p align="justify">
								O padrinho é um membro do grupo escolhido pelo residente para ajudá-lo na escrita do 4º Passo, assim como para ouvir o seu 5º Passo. Este residente deve estar na 2ª ou 3ª Fase do tratamento, de preferência com não muito mais tempo de tratamento do que o residente, podendo ter menos tempo, mas não menos de 3 meses de tratamento. Este padrinho pode ser o mesmo do Retorno de Confronto, caso ainda se encontre dentro da Comunidade Terapêutica (CT). É indicado que, além da confiança, o padrinho tenha o mesmo tipo de dependência (álcool, drogas, cruzada), idade semelhante e que seja da mesma cidade ou de cidades vizinhas, para facilitar o contato pós tratamento.
								<br />
								Este deverá seguir as instruções contidas no Regulamento do Padrinho de 5º Passo.
							</p>
							
							<div class="space-20"></div>
							
							<h4><strong>5º Passo</strong></h4>
							
							<p align="justify">
								Uma vez que o residente completa 5 meses de tratamento, e com o 4º Passo já escrito, tem início o 5º Passo, que consiste na leitura do 4º Passo, falando assim para o seu padrinho, para um membro da equipe interna e um da equipe de Psicologia, tudo o que foi escrito no decorrer dos dois meses anteriores, o que costuma demorar entre 10 a 20 horas, distribuídas em uma semana aproximadamente.
								<br />
								Este Passo tem dois objetivos principais: o desabafo e o autoconhecimento.
								<br />
								É claro que é uma tarefa extremamente difícil abrir todos os pormenores de sua vida para outra pessoa, mas muito alívio de consegue se tiver a coragem necessária para realizá-la. Por outro lado a busca do autoconhecimento é de fundamental importância neste processo, e isto acontece através do Retorno de 5º Passo, elaborado pela equipe de Psicologia após terminar este Passo.
							</p>
							
							<div class="space-20"></div>
							
							<h4><strong>5º Passo – Padrinho</strong></h4>
							
							<p align="justify">
								O mesmo Padrinho de 4º Passo, podendo ser outro em caso deste ter abandonado o tratamento ou caso o residente sinta necessidade, sempre sendo isto avaliado juntamente com a equipe de Psicologia.
							</p>
							
							<div class="space-20"></div>
							
							<h4><strong>5º Passo – Retorno</strong></h4>
							
							<p align="justify">
								Depois de completado o 5º Passo, passando por todas as quase 400 perguntas que o Guia propõe, é dada uma devolutiva pela equipe de Psicologia, chamada de retorno. Este é dado por escrito e verbalmente, em dois momentos diferentes.
								<br />
								A parte escrita está composta por uma tabela com duas colunas. A primeira, chamada Problema, refere-se aos problemas detectados, com uma visão profunda de sua vida, referindo conteúdos de mundo interno e dinâmicas familiares, e a segunda, chamada de Ação positiva, referente às ações concretas propostas para diminuir o impacto na vida do residente dos problemas relacionados na primeira coluna.
								<br />
								Depois da tabela são descritos os pormenores de cada um dos problemas indicados, assim como detalhadas as ações propostas. Estas ações podem ser diretivas, nos casos em que se faça necessário, ou também reflexivas, propondo um maior grau de autoconhecimento assim como uma ressignificação da sua história, bem como dos seus personagens principais.
								<br />
								A parte escrita é dada normalmente um dia antes da parte verbal ser realizada, para que o residente, juntamente com o seu padrinho, tenha tempo de ler tranquilamente e refletir por si mesmo ante os problemas e ações propostas. A parte verbal pode durar de 2 a 4 horas aproximadamente, dependendo da complexidade do retorno, assim como do interesse do residente em aprofundar os conteúdos indicados.
							</p>
							
							<div class="space-20"></div>
							
							<h4><strong>8º Passo – Relatório</strong></h4>
							
							<p align="justify">
								O 8º Passo propõe relacionar todos os danos que o residente possa ter causado a outras pessoas durante a sua dependência, e isto é feito através de um Relatório elaborado pelo Coordenador da CTNJ, e que dá sequência ao trabalho já iniciado com o 4º e 5º Passo.
								<br />
								Este relatório consiste numa tabela com 4 colunas: pessoa, dano, tipo, intensidade.
								<br />
								o Na coluna pessoa deve ser escrito o nome da pessoa que foi prejudicada.
								<br />
								o Na coluna dano deve ser descrito o dano causado, provavelmente já descrito durante o 5º Passo.
								<br />
								o Na coluna tipo deve ser avaliado o tipo de dano causado, de acordo com o estudado nas reuniões de 8º Passo, sendo estes tipos: moral, físico, material ou sexual.
								<br />
								o Na coluna intensidade deve se avaliar a intensidade do dano, também de acordo com o estudado nas reuniões de 8º Passo, sendo estas intensidades: circunstancial, psicológico, emocional.
								<br />
								O residente recebe este relatório quando retorna da 1ª visita de ressocialização, e deve descrever no mínimo 20 e no máximo 40 danos antes de sair para a próxima visita. Assim que termine este relatório, deve entregá-lo ao membro da equipe de Psicologia responsável pelo seu caso, e este o avaliará e fará as considerações necessárias antes de entregar o Relatório de 9º Passo.
							</p>
							
							<div class="space-20"></div>
							
							<h4><strong>9º Passo – Relatório</strong></h4>
							
							<p align="justify">
								É a continuação do Relatório de 8º Passo, e é entregue pela equipe de Psicologia quando o residente retorna da 2ª visita de Ressocialização, juntamente com o Relatório de 8º Passo corrigido e avaliado, e tem a finalidade de avaliar e relacionar as possíveis reparações que serão feitas em relação aos danos descritos.
								<br />
								Consiste numa tabela contendo as 4 colunas do relatório anterior, mais 5 colunas: possível/impossível, prejudica o outro, põe em risco a sobriedade, reparação direta/indireta, reparação.
								<br />
								Nas primeiras 4 colunas se repete o conteúdo do Relatório de 8º Passo, com as modificações propostas pela equipe de Psicologia, discutidas com o residente após o retorno da 1º visita.
								<br />
								Na coluna possível/impossível se avalia se a reparação é possível ou não, por exemplo em caso de morte da pessoa prejudicada, ou em caso de não ter contato com a mesma nem saber como encontra-la.
								<br />
								Na coluna prejudica o outro se avalia se fazer a reparação pode causar mais problemas do que resolvê-los e, se for o caso, se opta por um tipo de reparação indireta.
								<br />
								Na coluna põe em risco a sobriedade, ele avalia se ter contato com a pessoa prejudicada no passado, ou com o contexto em que esta se encontra, será prejudicial para sua recuperação neste momento e, se for o caso, se opta por um tipo de reparação indireta, ou por adiar a reparação até outro momento mais propício.
								<br />
								Na coluna reparação direta/indireta se avalia de forma definitiva, de acordo com os critérios anteriores, se a reparação vai ser feita diretamente com a pessoa prejudicada, ou se vai ser necessário realizar um tipo de reparação indireta, ou seja, uma outra forma de compensar o dano causado.
								<br />
								Na coluna reparação se descreve qual vai ser a reparação específica que pretende ser realizada.
								<br />
								Este relatório é entregue ao membro da equipe de psicologia responsável pelo caso antes de sair para a 3ª visita de ressocialização.
							</p>
							
							<div class="space-20"></div>
							
							<h4><strong>9º Passo – Retorno</strong></h4>
							
							<p align="justify">
								Após retornar da 3ª visita de ressocialização é realizada uma avaliação minuciosa do relatório de 9º Passo, juntamente com o membro da equipe de psicologia responsável pelo caso e com o padrinho de 5º Passo – se ainda estiver na CT – ou outro membro do grupo de escolha do residente.
								<br />
								Esta avaliação tem como finalidade traçar um plano de ação pós-tratamento em relação às reparações que devem ser feitas, de acordo com o definido pelo Relatório de 9º Passo. É avaliado qual reparação pode ser feita inicialmente, quais devem ser deixadas para momentos posteriores, assim como uma longa série de questões que dizem respeito ao início da ressocialização efetiva, uma vez concluído o tempo de tratamento.
							</p>
							
							<div class="space-20"></div>
							
							<h4><strong>Acolhimento – Reunião</strong></h4>
							
							<p align="justify">
								Esta reunião é direcionada para os residentes durante o 1º mês de tratamento, realizada por um membro da equipe interna, ou eventualmente por um membro da equipe de Psicologia, e tem como finalidade apresentar ao novo membro da casa todas as normas e atividades, horários e obrigações, que fazem parte do dia-a-dia da CT, a fim de que estes consigam compreender mais rapidamente o funcionamento do local onde agora se encontram.
								<br />
								É também um espaço onde eventualmente são discutidas as diferentes dificuldades de adaptação a estas regras e horários que estes novos residentes podem estar sentindo, podendo estes questionar o sentido das mesmas.
							</p>
